// Inicializa as variáveis
let indice = 12;
let soma = 0;
let k = 1;

// Enquanto k for menor que o valor de indice
while (k < indice) {
    k = k + 1; // Incrementa k
    soma = soma + k; // Adiciona k à soma
}

// Imprime o valor final de soma
console.log(`O valor da variável SOMA é ${soma}.`);


